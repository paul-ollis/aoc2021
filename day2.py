from itertools import accumulate
from typing import Iterator, Tuple


def parse_route(route: str) -> Iterator[Tuple[int, int]]:
    """Parse the route.

    This takes a sequence of route instructions (one per line) and converts it
    to a sequence of tuples of the form:

        (depth_change, distance_change)

    For part two of the puzzle, the tuple meaning is:

        (aim_change, impulse_value)
    """
    non_blank = (line.strip() for line in route.splitlines() if line.strip())
    for line in non_blank:
        command, value_str = line.split()
        if command == 'forward':
            yield 0, int(value_str)
        elif command == 'down':
            yield int(value_str), 0
        elif command == 'up':
            yield -int(value_str), 0
        else:
            raise RuntimeError(f'Unhandled command {command}')


def calc_position(route):
    """Calculate position, part 1."""
    depth = sum(d for d, _ in parse_route(route))
    position = sum(p for _, p in parse_route(route))

    print(depth * position)


def calc_position2(route):
    """Calculate position, part 2."""
    aims = accumulate(d for d, _ in parse_route(route))
    impulses = list(imp for _, imp in parse_route(route))
    depth = sum(aim * imp for aim, imp in zip(aims, impulses))
    position = sum(impulses)

    print(depth * position)


